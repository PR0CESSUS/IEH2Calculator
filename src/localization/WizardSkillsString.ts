export function WizardSkillsString(id) {
  switch (id) {
    case 0:
      return "";
    case 1:
      return "Hit Count +1 for this Skill";
    case 2:
      return "";
    case 3:
      return "MP Consumption -50% for this Skill";
    case 4:
      return "MP Gain +100% for this Skill";
    case 5:
      return "Cast Time -50% for this Skill";
    case 6:
      return "Placeholder";
    default:
      return "";
  }
}
